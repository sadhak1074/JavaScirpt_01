//Assignment operators used to assign values to variables.

let x = 10;
let y = 10;

console.log(x = y); //same as x = y 

console.log(x += y);//same as x = x + y 

console.log(x -= y);//same as x = x - y 

console.log(x *= y);//same as x = x * y 

console.log(x /= y);//same as x = x / y 

console.log(x %= y);//same as x = x % y 

console.log(x **= y);//same as x = x ** y 