//Arithmatic Operators(Used for mathmatical operations).
let x = 10;
let y = 20;

console.log(x+y); //Addition operator '+'

console.log(x-y); //Subtraction Operator '-'

console.log(x*y); //Multiplication Operator '*'

console.log(x/y); //Division Operator '/'

console.log(x%y); //Modulo Operater'%'it gives us remainder.

console.log(x**y); //Exponential operator'**' it gives the value of x^y.
