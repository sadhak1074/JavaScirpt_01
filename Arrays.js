//Declaring arrays
const fruits = ["Banana", "Apple", "Grapes"];
const a = [1, "Sadhak", true];
//Elements inside an array can be of different datatypes

//Printing the arrays
console.log(fruits);
console.log(a);

//Accessing values of the arrays
let numbers = [1, 2, 3, 4, 5];
console.log(numbers[0]); //prints first element in the list
console.log(numbers[4]); //prints last element in the list

//finding the length
console.log(numbers.length); //prints length of given numbers

//Changing the values in given array
console.log(numbers[2] = 8);

